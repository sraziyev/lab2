'''Use the pop method to remove "model" from the car dictionary.'''
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
car.pop("model")

'''Use the clear method to empty the car dictionary.'''
car =	{
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
car.clear()