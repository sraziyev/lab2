'''What is a correct syntax for looping through the values of this dictionary:'''
x = {'type' : 'fruit', 'name' : 'apple'}
for y in x.values():
  print(y)



'''What is a correct syntax for looping through the keys AND values of this dictionary:'''
x = {'type' : 'fruit', 'name' : 'apple'}
for y, z in x.items():
  print(y, z) 


